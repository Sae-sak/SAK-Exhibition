import "../styles/globals.css";
import type {AppProps} from "next/app";
import Head from "next/head";
import {ThemeProvider} from "next-themes";
import {Provider} from "react-redux";
import {store} from "@toolkit/store";
import localFont from "@next/font/local";
import {Roboto, Noto_Serif_KR, Nanum_Myeongjo} from "@next/font/google";

const amagro = localFont({
  src: "../assets/fonts/Amagro/Amagro.ttf",
  variable: "--font-amagro",
});

const nanumMyeongjo = Nanum_Myeongjo({
  weight: ["400", "700", "800"],
  subsets: ["korean"],
  variable: "--font-nanumMyeongjo",
});

const notoSerifKR = Noto_Serif_KR({
  weight: ["200", "300", "400", "500", "700"],
  subsets: ["korean"],
  variable: "--font-notoSerifKR",
});

const roboto = Roboto({
  weight: ["400", "700"],
  subsets: ["latin"],
  variable: "--font-roboto",
});

export default function App({Component, pageProps}: AppProps) {
  return (
    <>
      <Head>
        <title>SAK Exhibition</title>
        <meta
          name="description"
          content="SAK Exhibition Generated by Kim Yoo"
        />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <meta name="msapplication-TileColor" content="#ffffff" />
        <meta name="msapplication-TileImage" content="/ms-icon-144x144.png" />
      </Head>

      <ThemeProvider attribute="class">
        <Provider store={store}>
          <main
            className={` ${amagro.variable} ${nanumMyeongjo.className} ${notoSerifKR.className} ${roboto.className} `}
          >
            <Component {...pageProps} />
          </main>
        </Provider>
      </ThemeProvider>
    </>
  );
}
